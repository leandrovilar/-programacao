/*
    var - Ele é utilizado globalmente
    let - Ele é gerado dentro de um escopo
    const - Ele é gerado dendto de um escopo porém podemos armazenar dados apenas 1x
*/

var cachorro = "Rabito";
console.log(cachorro);

cachorro = "Golias"
console.log(cachorro);

let lanche = "bolovo";
console.log(lanche);

lanche = "Misto quente";
console.log(lanche);

const nome = "Leandro";
console.log(nome);

nome = "Leandro Vilar";
console.log(nome);

